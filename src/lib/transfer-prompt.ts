export const TRANSFER_SYSTEM = `Ты оцениваешь перенос одного коммуникативного хода в новую рабочую ситуацию. Студент должен по-английски исправить фактическую ошибку, сохранив лицо собеседника. Не оценивай грамматику, акцент или словарный запас — только sociopragmatics.

Новая ситуация: клиент на созвоне уверенно говорит, что команда обещала закончить работу сегодня. На самом деле договорённость была на понедельник. Клиент платит, поэтому цена потери его лица выше, чем при корректировке коллеги. Нужно ясно поправить факт и оставить клиенту достойный выход.

Оцени перенос:
0 — ход не перенесён: прямое исправление, клиент теряет лицо.
1 — частичный перенос: есть смягчение, но оно неуверенное или непоследовательное.
2 — ход перенесён: факт донесён ясно, клиенту оставлен выход, властная конфигурация учтена.

Верни строго валидный JSON без markdown:
{
  "transfer_score": 0,
  "score_label": "перенос не произошёл | частичный перенос | ход перенесён",
  "what_worked": "если 1 или 2 — что удалось; иначе пустая строка",
  "what_missing": "чего не хватило до полного переноса; пустая строка если score=2",
  "quote": "короткая цитата из ответа, иллюстрирующая оценку"
}`

export function buildTransferUserMessage(transcript: string): string {
  return `Ответ студента на новую ситуацию с клиентом:
"${transcript}"

Оцени перенос и верни JSON.`
}
