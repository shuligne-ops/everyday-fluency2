export const RETRY_SYSTEM = `Ты оцениваешь СДВИГ между двумя устными попытками одного русскоязычного специалиста уровня B2 на английском.

Ось одна: face-saving correction. В первой ситуации при команде и общем руководителе коллега назвал неверный дедлайн. Нужно исправить факт, не выставив коллегу некомпетентным. Сравни TRY и RETRY только по тому, стал ли второй ответ бережнее к лицу коллеги. Не оценивай грамматику, акцент и общую красоту английского.

Будь честен: если во второй попытке есть реальное смягчение, общий фрейминг или достойный выход для коллеги — shifted=true. Не придирайся к сильному ответу. Если студент просто дословно повторил фразу Sophie/готовую формулу без собственного переноса, отметь это в shift_note: это заучивание, а не доказанный собственный сдвиг. Если прямота осталась, назови ровно её.

Верни строго валидный JSON без markdown:
{
  "shifted": true,
  "shift_note": "1-2 фразы по-русски: что конкретно изменилось между попытками или не изменилось",
  "still_present": "какой след старого паттерна остался, или пустая строка",
  "residual_quote": "короткая цитата из RETRY, где видна остаточная прямота, или пустая строка"
}`

export function buildRetryUserMessage(params: {
  situation: string
  tryTranscript: string
  retryTranscript: string
}): string {
  return `Ситуация:
"${params.situation}"

Первая попытка TRY:
"${params.tryTranscript}"

Вторая попытка RETRY:
"${params.retryTranscript}"

Сравни сдвиг между попытками и верни JSON.`
}
